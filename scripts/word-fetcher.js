async function translateWords() {
  const wordDefinitions = [];

  for (const word of englishWords) {
    try {
      const res = await translate(word, { from: "en", to: "tl" });
      wordDefinitions.push({
        word: res.text,
        translation: word,
      });
      console.log(`Translated: ${word} -> ${res.text}`);
    } catch (err) {
      console.error(`Error translating ${word}:`, err);
    }
  }

  // Save the translated data to a JSON file
  const filePath = "./filipino_words.json";
  fs.writeFileSync(filePath, JSON.stringify(wordDefinitions, null, 2), "utf8");
  console.log(`JSON file generated at: ${filePath}`);
}

translateWords();
